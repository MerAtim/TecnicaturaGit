operandoA = 8
operandoB = 5
suma = operandoA + operandoB
print("Resultado de la suma es:",suma)
print(f'El resultado de la suma es: {suma}') # format # Colocar la variable dentro de la cadena se llama interpolacion

resta = operandoA - operandoB
print(f'El resultado de la resta es: {resta}')

multiplicacion = operandoA * operandoB
print(f'El resultado de la multiplicación es: {multiplicacion}')

division = operandoA / operandoB
print(f'El resultado de la division es: {division}')

division = operandoA // operandoB
print(f'El resultado de la division (int) es: {division}') # Resultado Sin decimales

modulo = operandoA % operandoB
print(f'El resultado del resto es: {modulo}')

exponente = operandoA ** operandoB
print(f'El resultado de la potenciación es: {exponente}')